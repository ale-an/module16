namespace Module16
{
    public class Calculator
    {
        public int Subtraction(int a, int b)
        {
            return a - b;
        }

        public int Division(int a, int b)
        {
            return a / b;
        }

        public int Addition(int a, int b, int c)
        {
            return a + b + c;
        }

        public int Multiplication(int a, int b, int c)
        {
            return a * b * c;
        }
    }
}